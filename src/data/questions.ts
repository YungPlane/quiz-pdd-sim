// Helper function to get random medicine ticket (10 questions)
export function getRandomMedicineTicket(): Question[] {
  // There are 30 tickets (0-29), each with 10 questions
  const ticketIndex = Math.floor(Math.random() * 30);
  const startIndex = ticketIndex * 10;
  return medQuestions.slice(startIndex, startIndex + 10);
}

